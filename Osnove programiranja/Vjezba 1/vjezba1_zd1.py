#varijabli broj pridružujemo vrijednost 7
broj=7.5e7

print('Cjelobrojna vrijednost= ',int(broj))
print('Decimalna vrijednost= ',float(broj))
print('Binarna vrijednost= ',bin(int(broj)))
print('Oktalna vrijednost= ',oct(int(broj)))
print('Heksadecimalna vrijednost= ',hex(int(broj)))
print('Logička vrijednost= ',bool(broj))
print('String= ',str(broj))